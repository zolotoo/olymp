CREATE TABLE IF NOT EXISTS public.weekly_sends (
  tg_id      BIGINT NOT NULL,
  week_num   SMALLINT NOT NULL,          -- 1,2,3,4
  sent_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  message_id BIGINT,                      -- TG message id для отладки
  PRIMARY KEY (tg_id, week_num)
);

ALTER TABLE public.weekly_sends DISABLE ROW LEVEL SECURITY;
GRANT ALL ON TABLE public.weekly_sends TO anon, authenticated, service_role;

