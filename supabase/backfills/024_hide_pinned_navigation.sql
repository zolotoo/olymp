-- Скрываем закрепы/навигационные шапки топиков и канала из Библиотеки.
-- Каждый из этих постов — это «навигация по разделу» / «правила раздела»,
-- их роль закрепом наверху ветки уже выполнена в Telegram, и в Библиотеке
-- они забивают первую карточку и выглядят дублём.
--
-- Если в будущем найдётся ещё что-то скрыть — повторяем тот же UPDATE
-- с нужными message_id.

UPDATE public.tg_messages SET is_hidden = true
WHERE
  -- Группа AI Олимп / Ветки (-1003828793815): закрепы видимых веток.
  --   167 — «Здесь все гайды и промты…» (Гайды и промты, thread 18)
  --   163 — «Здесь собраны бесплатные нейросети…» (Бесплатные нейронки, thread 8)
  --   162 — «Здесь будут публиковаться все эфиры…» (Эфиры, thread 16)
  --   169 — «Разбираем тренды в нейросетях…» (ИИ тренды, thread 5)
  (chat_id = -1003828793815 AND message_id IN (162, 163, 167, 169))
  OR
  -- Канал AI Олимп (-1003711772182): welcome + навигация по практикумам.
  --   6 — «Добро пожаловать в AI Олимп!»
  --   7 — «Навигация по всем практикумам»
  (chat_id = -1003711772182 AND message_id IN (6, 7));
